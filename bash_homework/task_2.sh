# 1. Выводим текущее значение переменной PATH
echo "Текущее значение переменной PATH:"
echo $PATH

# 2. Добавляем новую директорию, переданную как аргумент
if [ -z "$1" ]; then
    echo "Пожалуйста, укажите директорию для добавления в PATH."
    exit 1
fi

export PATH="$1:$PATH"
echo "Новая переменная PATH:"
echo $PATH

# 3. Объяснение временных изменений и способа сделать их постоянными
echo -e "\nИзменения переменной PATH, сделанные через терминал, временные, потому что они действуют только в текущем сеансе оболочки."
echo "При закрытии терминала или открытии нового окна терминала переменная PATH сбрасывается до первоначального значения."

# Чтобы сделать изменения постоянными, добавьте команду в файл .bashrc:
echo -e "\nЧтобы сделать изменения постоянными, добавьте следующую строку в файл ~/.bashrc:"
echo "export PATH=\"\$PATH:$1\""

# Команда для перезапуска терминала:
echo -e "\nПосле редактирования файла .bashrc выполните следующую команду для применения изменений:"
echo "source ~/.bashrc"