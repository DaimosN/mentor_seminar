# Запускаем три команды sleep в фоновом режиме
sleep 10 &
sleep 20 &
sleep 30 &

# Выводим список запущенных задач
echo "Запущенные задачи:"
jobs

# Приостанавливаем выполнение скрипта для наблюдения за задачами
read -p "Нажмите Enter, чтобы продолжить..."

# Переводим первую задачу в фон (если она не завершилась)
bg %1
echo "Перевели первую задачу в фон."

# Переводим вторую задачу в передний план
fg %2
echo "Вторая задача теперь выполняется в переднем плане."

# Завершаем выполнение скрипта
echo "Скрипт завершен."

# После запуска скрипта можно увидеть, что все три задачи выполняются параллельно.

# Использование команды jobs позволяет отследить состояние всех запущенных задач.
# Перевод задач из фона в передний план с помощью команды fg позволяет взаимодействовать с ними напрямую.
# Команда bg позволяет продолжить выполнение задачи в фоновом режиме, что полезно для управления несколькими процессами одновременно.